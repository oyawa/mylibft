#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	int str;

	i = 0;
	str = 0;
	while (s1[str] != '\0')
	{
		str++;
	}
	while (s2[i] && i < n != '\0')
	{
		s1[str] = s2[i];
		str++;
		i++;
	}
	s1[str] = '\0';
	return (s1);
}
